public class project_4_3 {

	public static void main(String[] args) {

		int score = 79;

		if (score < 40)
			System.out.println("E");
		else if (score < 50)
			System.out.println("F");
		else if (score < 60)
			System.out.println("D");
		else if (score < 70)
			System.out.println("C");
		else if (score < 80)
			System.out.println("B");
		else
			System.out.println("A");

	}
}
